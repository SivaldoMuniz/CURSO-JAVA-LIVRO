public class SaidaFormatada {
	public static void main (String[] args){
		System.out.println();

		System.out.printf("Uma caractere: %c", 'S');
		System.out.println();
		System.out.printf("Um texto: %s", "Java em todos os lugares");
 		System.out.println();
		System.out.printf("Um número inteiro: %d", 123);
		System.out.println();
		System.out.printf("Um número decimal:%f", 4.55);
		System.out.println();
		System.out.printf("Um valor booleano: %b", true);
		

		System.out.println();

			}
		}
